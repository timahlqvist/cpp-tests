#include <iostream>
#include <cmath>

using namespace std;

int main(){

    cout << pow(5, 3) << endl; // 5 upphöjt till 3
    cout << sqrt(9) << endl;   // kvadratroten av 9

    float x = 2.14;

    cout << ceil(x) << endl;   // Avrundning uppåt
    cout << floor(x) << endl;  // Avrundning neråt

    return 0;
}
